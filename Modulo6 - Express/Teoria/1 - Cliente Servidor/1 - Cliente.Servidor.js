/*

    Cliente/Servidor:

    Llamamos Cliente: al dispositivo que hace peticiones de ciertos servicios y recursos a un servidor
    (Puede ser una pc, celular, consola de videojuegos, cualquiera que pueda conectarse a una red)

    Llamamos Servidor: al equipo que brinda servicios a los dispositivos que se hayan conectado con el mediante la misma red
    (Retorna muchas veces una pagina web)

    Esta arquitectura cliente-servidor hace referencia a un modelo de comuncacion que vincula a varios dispositivos con un servidor a traves de una red (internet)
    (cada vez que escribimos www.facebook.com hacemos una request a los servidores de facbook que envian la response)
    (asi como accedemos a un link que cambiamos /algo o enviamos un form, todo envia peticiones)

    Los servidores son computadoras que tienen instalado un webserver
    (programa que se encarga de procesar los pedidos y envar las respuestas)

*/