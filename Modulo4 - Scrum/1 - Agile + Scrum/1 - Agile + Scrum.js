//Manifiesto Agil

/*
    - 2001 los impulsores de distintas metodologias (antes conocidas como blandas) 
    - Se pusieron de acuerdo en crear el manifiesto agil.

    - Es un compromiso publico 
    - Para lograr las mejores formas de desarrollar software 
    - Y ayudar a otros a implementarlas
    - 4 valores y 12 principios

    valores:
    Personas e interacciones    >   Procesos y herramientas
    Software funcional          >   Documentacion compleja
    Colaboracion con el cliente >   Apego a contratos
    Responder ante el cambio    >   Seguir un plan

    caracteristicas:
    - orientadas a satisfacer al cliente
    - se le entrega una version minima del producto lo mas rapido posible
      y se mejora esa version a traves de los ciclos cortos del desarrollo
    - cada ciclo apunta a etregar una version incremental del proyecto
      lo que no esta terminado no se entrega
    - el cambio es bienvenido aun en etapas avanzadas
    - equipos autoorganizados
      entorno productivo con buena comunicacion 
    - mejora constante
      refleccion sobre el trabajo realizado, procesos implementados y la exp del equipo
    - simplicidad
      maximizar resultado y minimizar el esfuerzo
*/

//SCRUM

/*
    - facil de comprender e implementar
    - fomenta el trabajo en equipo, responsibilidad, visibilidad del trabajo
    
    roles:
    - product owner, representa la voz del cliente y toma las deciciones finales del producto
    - scrum master, asegurar el intercambio eficiente de la info, 
      controla el tiempo, conversaciones y el proceso en gral.
    - equipo de desarrollo o tecnico, 3 o 9 personas guiado por el scrum master 
      y recibiran sus tareas del product owner todas las decisiones se toman por consenso 
    - stakeholders o accionistas, aquellas persoas que usan o seran afectadas por nuestro producto o servicio

    flujo de trabajo:
    -backlog, cjto de requisitos del proyecto. (gestionada por el product owner)
    -sprint backlog, requisitos desarrollados en el sprint actual (gestionada por el equipo de desarrollo)
    -sprint: duracion siempre la misa y definida por el equipo 2-4 weeks
    
    tablero de tareas:
    -historias de usuario: describir funcionalidad o comportamiento vista desde los ojos del usuario
    como[rol de usuario]-quiero[objtivo]-para poder[beneficio]
    ceremonias:
    -la planificacion: se decide el trabajo que se hara durante el sprint
    -reunion diaria: que hice ayer, tuve impedimentos, que voy a hacer hoy
    -revicion o demo: revisar el trabajo para presentarlo
    -retrospectiva
*/
